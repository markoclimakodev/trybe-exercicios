/**8 - Utilize if/else para escrever um código que defina três números em variáveis
 * e retorne true se pelo menos uma das três for par.
 * Caso contrário, o código deve retornar false. */

let numberOne = 11;
let numberTwo = 7;
let numberThree = 230;

if (numberOne % 2 === 0 || numberTwo % 2 === 0 || numberThree % 2 === 0) {
  console.log(true);
} else {
  console.log(false);
}
